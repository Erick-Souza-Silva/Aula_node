let nome = "erick"; // Declarando uma variavel do tipo string.

console.log(nome.toUpperCase()); // Imprimindo o valor da variavel nome em maiusculo.

console.log(nome.toLowerCase()); // Imprimindo o valor da variavel nome em minusculo.

console.log(nome.charAt(0)); // Imprimindo o valor da variavel nome na posicao 0 (primeira letra).
console.log(nome.charAt(1)); // Imprimindo o valor da variavel nome na posicao 1 (segunda letra).
console.log(nome.charAt(2)); // Imprimindo o valor da variavel nome na posicao 2 (terceira letra).
console.log(nome.charAt(3)); // Imprimindo o valor da variavel nome na posicao 3 (quarta letra).
console.log(nome.charAt(4)); // Imprimindo o valor da variavel nome na posicao 4 (quinta letra).

console.log(nome.length); // Imprimindo o tamanho da variavel nome (quantidade de letras).

