numero = [1,2,3,4,5]; // Declaracao de um array chamado "numero" que armazena cinco elementos numericos.


for (let i = 0; i < numero.length; i++) {
    console.log(numero[i]); // Acessando cada elemento do array "numero" usando um loop for

} // Estruturas de repeticao sao usadas para executar um bloco de codigo varias vezes, ate que uma condicao especifica seja atendida. Neste caso, o loop for percorre cada elemento do array "numero" e imprime seu valor no console.
